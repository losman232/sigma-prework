# program that fins min and max value of a list

# list of numbers
numbers = [-100, 100]
# sort list
sorted_numbers = sorted(numbers)
# 1st item in sorted list is the smallest
min_val = sorted_numbers[0]
# last item in sorted list is the largest
max_val = sorted_numbers[-1]
# Display age
print("min number:", min_val)
print("max number:", max_val)
